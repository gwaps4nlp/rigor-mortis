<h1 class="text-center">À propos de Rigor Mortis</h1>

<h2>Quoi ?</h2>
<p>
Rigor Mortis est un jeu dont le but est d'annoter des unités polylexicales et de mesurer l'intuition des locuteurs concernant ces unités.
Les points peuvent être gagnés non seulement en annotant beaucoup de phrases, mais surtout en les annotant correctement.
</p>


<p>
Avant de jouer, lisez la {!! link_to('charter','charte') !!} de Rigor Mortis.
</p>



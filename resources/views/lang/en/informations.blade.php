<h1 class="text-center">About ZombiLingo</h1>

<h2>What is ZombiLingo?</h2>
<p>
ZombiLingo is a Game With A Purpose which aims to annotate corpora with dependency syntax relations as well as to gather other language data.
Points (brains) are won when annotating sentences, and annotating them well will help you gain even more. Conversely, bad annotations will generate a waste of time and points.
</p>

<p>
ZombiLingo offers other ways to win points that you will discover while playing.
This game is intended for people interested in language and grammar, as well as zombie addicts.
</p>

